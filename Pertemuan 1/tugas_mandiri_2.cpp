#include <stdio.h>
int main() 
{
    
    int panjang = 10;
    int lebar = 7;

    // Algoritma Penjumlahan
    int luas = panjang * lebar;

  
    printf("Panjang : %d cm\n", panjang);
    printf("Lebar   : %d cm\n", lebar);
    printf("Luas Persegi Panjang   : %d cm^2\n", luas);

    return 0;
}
